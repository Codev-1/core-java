package my;

public class Switchkeyword {

	public static void main(String[] args) {
		int x=3;
		switch(x)
		{
		case 1:System.out.println("one");
		break;
		case 2:System.out.println("two");
		break;
		case 3:System.out.println("three");
		break;
		default:System.out.println("Other than 1,2,3");
		break;
		}

	}

}
